  private boolean isValidInput() {
      ArrayList message = null;
        message = new ArrayList(0);
        if (userName.isBlank() || !(userName.matches("^[a-zA-Z]*$"))){
            message.add("Username cannot be blank, contain digits or special characters. Please enter a valid name and try again");
        }
        if (password.isBlank() || !(password.matches("^[a-zA-Z]*$"))){
            message.add("Password cannot be blank, contain digits or special characters. Please enter a valid name and try again");
        }
                if(!(message.isEmpty())){
            JOptionPane.showMessageDialog(this, message.toArray());
            message.clear();
            return false;
        }
        else{
                return true;
            }
  }
    

